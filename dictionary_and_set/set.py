# Set is the colection of unordered items
# each element in the set must be unique and immutable
# list and dictionary cannot be stored in a set because they are mutable

collection = {1, 2, 2, 2, 3, 4, "hello", "world"}
print(collection)       # ignore duplicate items
print(len(collection))
print(type(collection))

collektion = {}     # empty dictionary
collektion = set()  # empty set
print(type(collektion))

# sets are mutable but elements in set are immutable